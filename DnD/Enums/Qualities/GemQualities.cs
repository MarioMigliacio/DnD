namespace DnD.Enums.Qualities
{
    /// <summary>
    /// Represents the particular quality state of a Gem item.
    /// </summary>
    public enum GemQuality
    {
        /// <summary>
        /// A gem of really bad quality.
        /// </summary>
        Chipped,

        /// <summary>
        /// A gem of low level quality.
        /// </summary>
        Flawed,

        /// <summary>
        /// A gem which is particularly valuable.
        /// </summary>
        Normal,

        /// <summary>
        /// A gem that is said to be of high quality, quite rare.
        /// </summary>
        Flaweless,

        /// <summary>
        /// A very rare quality of gem, only seen by the highest caliber of jeweler.
        /// </summary>
        Perfect
    }
}
