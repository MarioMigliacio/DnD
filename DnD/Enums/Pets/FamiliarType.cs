namespace DnD.Enums.Pets
{
    /// <summary>
    /// The various types of Wizard or Sorcerer Familiars available.
    /// </summary>
    public enum FamiliarType
    {
        /// <summary>
        /// A Bat companion. Provides +3 bonus on Listen for the master.
        /// </summary>
        Bat,

        /// <summary>
        /// A Cat companion. Provides +3 bonus on MoveSilently for the master.
        /// </summary>
        Cat,

        /// <summary>
        /// A Hawk companion. Provides +3 bonus on Spot in bright light for the master.
        /// </summary>
        Hawk,

        /// <summary>
        /// A Lizard companion. Provides +3 bonus on Climb for the master.
        /// </summary>
        Lizard,

        /// <summary>
        /// An Owl companion. Provides +3 bonus on Spot in darkness for the master.
        /// </summary>
        Owl,

        /// <summary>
        /// A Rat companion. Provides +2 bonus for Fortitude saves for the master.
        /// </summary>
        Rat,

        /// <summary>
        /// A Raven companion. Provides +3 bonus on Appraise for the master.
        /// </summary>
        Raven,

        /// <summary>
        /// A Snake companion. Provides +3 bonus on Bluff for the master.
        /// </summary>
        Snake,

        /// <summary>
        /// A Toad companion. Provides +3 bonus to hit points for the master.
        /// </summary>
        Toad,

        /// <summary>
        /// A Rat companion. Provides +2 bonus for Reflex saves for the master.
        /// </summary>
        Weasel
    }
}
