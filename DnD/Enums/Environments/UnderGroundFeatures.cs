namespace DnD.Enums.Environments
{
    /// <summary>
    /// Various supported Under ground features for dungeon caves.
    /// </summary>
    public enum UnderGroundFeature
    {
        /// <summary>
        /// Represents a rock-pillar feature in a cave.
        /// </summary>
        Column,

        /// <summary>
        /// Represents an eroded flat or rounded rock surface with unique colorations.
        /// </summary>
        Flowstone,

        /// <summary>
        /// Represents a mineral that has flower like features and unique colorations.
        /// </summary>
        GypsumFlower,

        /// <summary>
        /// Represents a collection of water large or small.
        /// </summary>
        Pool,

        /// <summary>
        /// Represents a structure hanging from the ceiling formed of calcium salts deposited by dripping water.  
        /// </summary>
        Stalactite,

        /// <summary>
        /// Represents a structure protruding from the ground formed of calcium salts deposited by dripping water.
        /// </summary>
        Stalagmite
    }
}
